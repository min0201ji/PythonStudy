# 리스트 생성 문법
# 리스트 식별자 = [요소값1, 요소값2, ... 요소값n]
# 리스트 크기는 가변적(넣는 만큼 만들어 진다!)
# 다양한 종류의 데이터를 하나의 리스트에 저장 가능(어떤 종류라도 가능! 예) 숫자, '문자')

scores = [100] # 리스트
print(scores)

scores = 100 # 변수
print(scores)

scores = [100, 200, 300]
print(scores)
print(scores[1])

